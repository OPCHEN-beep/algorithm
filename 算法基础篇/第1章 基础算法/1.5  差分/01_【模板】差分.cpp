#include <iostream>

using namespace std;

typedef long long LL;

const int N = 1e5 + 10;

int n, m;
LL f[N];//差分数组

int main()
{
    cin >> n >> m;
    //创建差分数组（利用性质）
    for(int i = 1; i <= n; i++)
    {
        LL x;
        cin >> x;
        f[i] += x;
        f[i + 1] -= x;
    }
    
    //处理m次修改操作
    while(m--)
    {
        LL l, r, k;
        cin >> l >> r >> k;
        f[l] += k;
        f[r + 1] -= k;
    }
    
    //还原出原始的数组
    for(int i = 1; i <= n; i++)
    {
        f[i] = f[i - 1] + f[i];
        cout << f[i] << " ";
    }
    
    return 0;
}
