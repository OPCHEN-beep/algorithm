//# P10452 货仓选址
//
//## 题目描述
//
//在一条数轴上有 $N$ 家商店，它们的坐标分别为 $A_1 \sim A_N$。
//
//现在需要在数轴上建立一家货仓，每天清晨，从货仓到每家商店都要运送一车商品。
//
//为了提高效率，求把货仓建在何处，可以使得货仓到每家商店的距离之和最小。
//
//## 输入格式
//
//第一行输入整数 $N$。
//
//第二行 $N$ 个整数 $A_1 \sim A_N$。
//
//## 输出格式
//
//输出一个整数，表示距离之和的最小值。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//4
//6 2 9 1
//```
//
//### 输出 #1
//
//```
//12
//```
//
//## 说明/提示
//
//数据保证，$1 \le N \le 10^5$，$0 \le A_i \le 40000$。

#include <iostream>
#include <algorithm>
using namespace std;

typedef long long LL;

const int N = 1e5 + 10;

int n;
LL a[N];

int main()
{
    cin >> n;
    for(int i = 1; i <= n; i++) cin >> a[i];
    sort(a + 1, a + 1 + n);
    LL ret = 0;

    //利用中间值计算
    // for(int i = 1; i <= n; i++)
    // {
    //     ret += abs(a[i] - a[n / 2]);
    // }
    // cout << ret << endl;

    //利用结论计算
    for(int i = 1; i <= n / 2; i++)
    {
        ret += a[n - i + 1] - a[i];
    }
    cout << ret << endl;
    
    return 0;
}
