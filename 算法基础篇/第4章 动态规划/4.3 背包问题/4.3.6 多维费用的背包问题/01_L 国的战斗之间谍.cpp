//# P1910 L 国的战斗之间谍
//
//## 题目背景
//
//L 国即将与 I 国发动战争！！
//
//## 题目描述
//
//俗话说的好：“知己知彼，百战不殆”。L 国的指挥官想派出间谍前往 I 国，于是，选人工作就落到了你身上。
//
//你现在有 $N$ 个人选，每个人都有这样一些数据：$A$（能得到多少资料）、$B$（伪装能力有多差）、$C$（要多少工资）。已知敌人的探查间谍能力为 $M$（即去的所有人 $B$ 的和要小于等于 $M$）和手头有 $X$ 元钱，请问能拿到多少资料？
//
//## 输入格式
//
//第一行三个整数 $N, M, X$ 代表总人数，敌国侦察能力和总钱数。
//
//第二行至第 $N + 1$ 行，每行三个整数 $A_i, B_i, C_i$ 分别表示第 $i$ 个人能得到的资料，他的伪装能力有多差和他要的工资。
//
//## 输出格式
//
//一行一个整数表示能得到的资料总数。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//3 10 12
//10 1 11
//1 9 1
//7 10 12
//```
//
//### 输出 #1
//
//```
//11
//```
//
//## 说明/提示
//
//对于 $100\%$ 的数据，$1\le N\le 100$，$1\le M\le 1000$，$1\leq X\leq 1000$。

#include <iostream>

using namespace std;

const int N = 110, M = 1010;

int n, m, x;
int a[N], b[N], c[N];
int f[M][M];

int main()
{
    cin >> n >> m >> x;

    for(int i = 1; i <= n; i++)
    {
        cin >> a[i] >> b[i] >> c[i]; 
    }

    for(int i = 1; i <= n; i++)
        for(int j = m; j >= b[i]; j--)
            for(int k = x; k >= c[i]; k--)
                f[j][k] = max(f[j][k], f[j - b[i]][k - c[i]] + a[i]);

    cout << f[m][x] << endl;

    return 0;
}
