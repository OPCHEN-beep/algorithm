//# B2120 单词的长度
//
//## 题目描述
//
//输入一行单词序列，相邻单词之间由 $1$ 个或多个空格间隔，请对应地计算各个单词的长度。
//
//注意：如果有标点符号（如连字符，逗号），标点符号算作与之相连的词的一部分。没有被空格间开的符号串，都算作单词。
//
//## 输入格式
//
//一行单词序列，最少 $1$ 个单词，最多 $300$ 个单词，单词之间用至少 $1$ 个空格间隔。单词序列总长度不超过 $1000$。
//
//## 输出格式
//
//依次输出对应单词的长度，之间以逗号间隔。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//She was born in 1990-01-02  and  from Beijing city.
//```
//
//### 输出 #1
//
//```
//3,3,4,2,10,3,4,7,5
//```

#include <iostream>
#include <string>

using namespace std;

int main()
{
    string s;
    bool flag = true;

    while(cin >> s)
    {
        if(flag)
        {
            cout << s.size();
            flag = false;
        }
        else
        {
            size_t n = s.size();
            cout << "," << n;
        }
    }

    return 0;
}
