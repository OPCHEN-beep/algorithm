//# B2092 开关灯
//
//## 题目描述
//
//假设有 $N$ 盏灯（$N$ 为不大于 $5000$ 的正整数），从 $1$ 到 $N$ 按顺序依次编号，初始时全部处于开启状态；第一个人（$1$ 号）将灯全部关闭，第二个人（$2$ 号）将编号为 $2$ 的倍数的灯打开，第三个人（$3$ 号）将编号为 $3$ 的倍数的灯做相反处理（即，将打开的灯关闭，将关闭的灯打开）。依照编号递增顺序，以后的人都和 $3$ 号一样，将凡是自己编号倍数的灯做相反处理。问当第 $N$ 个人操作完之后，有哪些灯是关闭着的？
//
//## 输入格式
//
//输入为一行，一个整数 $N$，为灯的数量。
//
//## 输出格式
//
//输出为一行，按顺序输出关着的灯的编号。编号与编号之间间隔一个空格。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//10
//```
//
//### 输出 #1
//
//```
//1 4 9
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//5
//```
//
//### 输出 #2
//
//```
//1 4
//```

#include <iostream>
#include <cstdio>
#include <cstring>
using namespace std;

const int N = 5010;
int arr[N] = {0};

int main()
{
    int n = 0;
    cin >> n;
    int i = 0;
    for(i = 2; i<=n; i++)
    {
        int j = 0;
        for(j = i; j <= n; j++)
        {
            if(j % i == 0)
            {
                if(arr[j] == 1)
                    arr[j] = 0;
                else
                    arr[j] = 1;
            }
        }
    }
    for(int i = 1; i <= n; i++)
    {
        if(arr[i] == 0)
            cout << i << " ";
    }

    cout << endl;

    return 0;
}
