//# B2113 输出亲朋字符串
//
//## 题目描述
//
//亲朋字符串定义如下：给定字符串 $s$ 的第一个字符的 ASCII 值加第二个字符的 ASCII 值，得到第一个亲朋字符；$s$ 的第二个字符加第三个字符得到第二个亲朋字符；依此类推。注意：亲朋字符串的最后一个字符由 $s$ 的最后一个字符 ASCII 值加 $s$ 的第一个字符的 ASCII 值。
//
//## 输入格式
//
//输入一行，一个长度大于等于 $2$，小于等于 $100$ 的字符串。
//
//## 输出格式
//
//输出一行，为变换后的亲朋字符串。输入保证变换后的字符串只有一行。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//1234
//```
//
//### 输出 #1
//
//```
//cege
//```

#include <iostream>
#include <cstdio>
#include <cstring>
using namespace std;

const int N = 110;
char str[N];

int main()
{
    cin >> str;
    int i = 0;
    while(str[i + 1])
    {
        char tmp = str[i] + str[i+1];
        cout << tmp;
        i++;
    }

    cout << (char)(str[i] + str[0]) << endl;
    return 0;

    return 0;
}
