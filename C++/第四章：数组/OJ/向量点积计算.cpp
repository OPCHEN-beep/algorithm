//# B2091 向量点积计算
//
//## 题目描述
//
//在线性代数、计算几何中，向量点积是一种十分重要的运算。
//
//给定两个 $n$ 维向量 $a=(a_1,a_2, \cdots ,a_n)$ 和 $b=(b_1,b_2, \cdots ,b_n)$，求点积 $a$ ・ $b=a_1b_1+a_2b_2+ \cdots +a_nb_n$。
//
//## 输入格式
//
//第一行是一个整数 $n$。$1 \le n \le 1000$。
//
//第二行包含 $n$ 个整数 $a_1,a_2, \cdots ,a_n$。
//
//第三行包含 $n$ 个整数 $b_1,b_2, \cdots ,b_n$。
//
//相邻整数之间用单个空格隔开。每个整数的绝对值都不超过 $1000$。
//
//## 输出格式
//
//一个整数，即两个向量的点积结果。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//3
//1 4 6
//2 1 5
//```
//
//### 输出 #1
//
//```
//36
//```


#include <iostream>
#include <cstdio>

using namespace std;

int arr1[1000] = {0};
int arr2[1000] = {0};

int main()
{
    int n, tmp, total;
    total = 0;
    cin >> n;
    for(int i = 0; i < n; i++)
    {
        cin >> arr1[i];
    }
    for(int i = 0; i < n; i++)
    {
        cin >> arr2[i];
    }

    for(int i = 0; i < n; i++)
    {
        tmp = arr1[i] * arr2[i];
        total += tmp;
    }
    cout << total << endl;

    return 0;
} 
