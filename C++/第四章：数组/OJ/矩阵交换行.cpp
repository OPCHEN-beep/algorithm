//# B2099 矩阵交换行
//
//## 题目描述
//
//给定一个 $5 \times 5$ 的矩阵(数学上，一个 $r \times c$ 的矩阵是一个由 $r$ 行 $c$ 列元素排列成的矩形阵列)，将第 $n$ 行和第 $m$ 行交换，输出交换后的结果。
//
//## 输入格式
//
//输入共 $6$ 行，前 $5$ 行为矩阵的每一行元素,元素与元素之间以一个空格分开。
//
//第 $6$ 行包含两个整数 $m$、$n$，以一个空格分开（$1 \le m,n \le 5$）。
//
//## 输出格式
//
//输出交换之后的矩阵，矩阵的每一行元素占一行，元素之间以一个空格分开。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//1 2 2 1 2
//5 6 7 8 3
//9 3 0 5 3
//7 2 1 4 6
//3 0 8 2 4
//1 5
//```
//
//### 输出 #1
//
//```
//3 0 8 2 4
//5 6 7 8 3
//9 3 0 5 3
//7 2 1 4 6
//1 2 2 1 2
//```

#include <iostream>
#include <cstdio>
using namespace std;

int arr[5][5];

int main()
{
    int m, n;
    for(int i = 0; i < 5; i++)
    {
        for(int j = 0; j < 5; j++)
        {
            cin >> arr[i][j];
        }
    }
    
    cin >> m >> n;
    for(int i = 0; i < 5; i++)
    {
        int tmp = arr[m - 1][i];
        arr[m - 1][i] = arr[n - 1][i];
        arr[n - 1][i] = tmp;
    }

  
    
    for(int i = 0; i < 5; i++)
    {
        for(int j = 0; j < 5; j++)
        {
            cout << arr[i][j] << " ";
        }
        cout << endl;
    }

    return 0;
}
