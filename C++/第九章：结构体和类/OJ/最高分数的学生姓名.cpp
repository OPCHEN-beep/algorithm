//# B2125 最高分数的学生姓名
//
//## 题目描述
//
//输入学生的人数，然后再输入每位学生的分数和姓名，求获得最高分数的学生的姓名。
//
//## 输入格式
//
//第一行输入一个正整数 $N$（$N \le 100$），表示学生人数。
//
//接着输入 $N$ 行，每行包括一个整数和一个字符串，用空格分开，代表分数姓名。
//
//分数是一个非负整数，且小于等于 $100$；
//
//姓名为一个连续的字符串，中间没有空格，长度不超过 $20$。
//
//数据保证所有同学的姓名都不同，而且最高分只有一位同学。
//
//## 输出格式
//
//获得最高分数同学的姓名。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//5
//87 lilei
//99 hanmeimei
//97 lily
//96 lucy
//77 jim
//```
//
//### 输出 #1
//
//```
//hanmeimei
//```
//
//## 说明/提示
//
//hanmeimei 的分数最高 $99$ 分。

#include <iostream>
#include <string>
#include <algorithm>
using namespace std;

const int N = 110;

struct Stu
{
    int score;
    string name;
}arr[N];

//降序
bool cmp(struct Stu s1, struct Stu s2)
{
    return s1.score > s2.score;
}


int main()
{
    int n = 0;
    cin >> n;
    for(int i = 0; i < n; i++)
    {
        cin >> arr[i].score >> arr[i].name;
    }
    sort(arr, arr + n, cmp);
    cout << arr[0].name << endl;

    return 0;
}
