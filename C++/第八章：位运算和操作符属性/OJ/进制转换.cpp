//# P1143 进制转换
//
//## 题目描述
//
//请你编一程序实现两种不同进制之间的数据转换。
//
//## 输入格式
//
//共三行，第一行是一个正整数，表示需要转换的数的进制 $n\ (2\le n\le 16)$，第二行是一个 $n$ 进制数，若 $n>10$ 则用大写字母 $\verb!A!\sim \verb!F!$ 表示数码 $10\sim 15$，并且该 $n$ 进制数对应的十进制的值不超过 $10^9$，第三行也是一个正整数，表示转换之后的数的进制 $m\ (2\le m\le 16)$。
//
//## 输出格式
//
//一个正整数，表示转换之后的 $m$ 进制数。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//16
//FF
//2
//```
//
//### 输出 #1
//
//```
//11111111
//```

#include <iostream>
#include <cmath>
#include <string>
using namespace std;

string s = "0123456789ABCDEF";

void x_to_m(int x, int m)
{
    //1.x/m的数字转换成m进制
    if(x >= m)
    {
        x_to_m(x/m, m);
    }
    cout << s[x % m];
}

int main()
{
    int n;
    string s;
    int m;
    cin >> n;
    cin >> s;
    cin >> m;
    //1.将s中存放的进制数字转换成10进制数字
    int x = stoi(s, NULL, n);
    //2.将x转换成m进制
    x_to_m(x, m);

    return 0;
}
