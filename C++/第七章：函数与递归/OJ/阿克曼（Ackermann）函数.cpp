//# B2144 阿克曼（Ackermann）函数
//
//## 题目描述
//
//阿克曼（Ackermann）函数 $A(m,n)$ 中，$m, n$ 定义域是非负整数（$m \le 3$，$n \le 10$），函数值定义为：
//
//$\mathit{A}(m,n)=n+1$；（$m=0$ 时）。
//
//$\mathit{A}(m,n)=\mathit{A}(m-1,1)$；（$m>0$、$n=0$ 时）。
//
//$\mathit{A}(m,n)=\mathit{A}(m-1,\mathit{A}(m,n-1))$；（$m,n>0$ 时）。
//
//## 输入格式
//
//$m$ 和 $n$。
//
//## 输出格式
//
//函数值。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//2 3
//```
//
//### 输出 #1
//
//```
//9
//```

#include <iostream>
#include <cstdio>
using namespace std;

int akm(int m, int n)
{
    if(m == 0)
        return n + 1;
    else if(m > 0 && n == 0)
        return akm(m - 1, 1);
    else if(m > 0 && n > 0)
        return akm(m - 1, akm(m, n - 1));
}

int main()
{
    int m, n;
    cin >> m >> n;
    cout << akm(m, n) << endl;

    return 0;
}
