#include <iostream>
using namespace std;


int math(int x, char c, int y)
{
    switch(c)
    {
        case '+':
            return x + y;
        case '-':
            return x - y;
        case '*':
            return x * y;
        case '/':
            return x / y;
        case '%':
            return x % y;    
    }
    
}

int main()
{
    int a = 0;
    int b = 0;
    char c = 0;
    cin >> a >> c >> b;
    cout << math(a, c, b) << endl;

    return 0;
}
