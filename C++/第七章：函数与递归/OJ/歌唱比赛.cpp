//# P5738 【深基7.例4】歌唱比赛
//
//## 题目描述
//
//$n(n\le 100)$ 名同学参加歌唱比赛，并接受 $m(m\le 20)$ 名评委的评分，评分范围是 $0$ 到 $10$ 分。这名同学的得分就是这些评委给分中去掉一个最高分，去掉一个最低分，剩下 $m-2$ 个评分的平均数。请问得分最高的同学分数是多少？评分保留 $2$ 位小数。
//
//## 输入格式
//
//第一行两个整数 $n,m$。   
//接下来 $n$ 行，每行各 $m$ 个整数，表示得分。
//
//## 输出格式
//
//输出分数最高的同学的分数，保留两位小数。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//7 6
//4 7 2 6 10 7
//0 5 0 10 3 10
//2 6 8 4 3 6
//6 3 6 7 5 8
//5 9 3 3 8 1
//5 9 9 3 2 0
//5 8 0 4 1 10
//```
//
//### 输出 #1
//
//```
//6.00
//```

#include <iostream>
#include <algorithm>
using namespace std;

int main()
{
    
    int m, n;
    int c;
    double top = 0;
    double tmp = 0;
    cin >> n >> m;
    for(int i = 0; i < n; i++)
    {
        int sum = 0;
        int _max = 0;
        int _min = 10;

        for(int j = 0; j < m; j++)
        {

            cin >> c;
            sum += c;
            _max = max(c, _max);
            _min = min(c, _min);
        }
        tmp = (sum - _max - _min) * 1.0 / (m - 2);
        top = max(tmp, top);
    }

    printf("%.2lf\n", top);
    
    return 0;
}
