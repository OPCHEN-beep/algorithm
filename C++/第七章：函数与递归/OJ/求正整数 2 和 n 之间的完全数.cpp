//# B2127 求正整数 2 和 n 之间的完全数
//
//## 题目描述
//
//求正整数 $2$ 和 $n$ 之间的完全数（一行一个数）。
//
//完全数：对于一个自然数，所有比它小的所有因数之和，等于它本身，它就是个完全数。如 $6=1+2+3$。
//
//## 输入格式
//
//输入 $n(n \le 10000)$。
//
//## 输出格式
//
//一行一个数，按由小到大的顺序。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//10
//```
//
//### 输出 #1
//
//```
//6
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//100
//```
//
//### 输出 #2
//
//```
//6
//28
//```

#include <iostream>
using namespace std;

bool PerfectNum(int n)
{
    int j = 0;
    int sum = 0;
    for(j = 1; j < n; j++)
    {
        if(n % j == 0)
            sum += j;
    }
    return sum == n;
}

int main()
{
    int n = 0;
    cin >> n;
    int i = 0;
    for(i = 2; i <= n; i++)
    {
        if(PerfectNum(i))
        {
            cout << i << endl;
        }
    }

    return 0;
}
