//题目描述
//甲流并不可怕，在中国，它的死亡率并不是很高。请根据截止 2009 年 12 月 22 日各省报告的甲流确诊数 a 和死亡数 b，计算甲流在各省的死亡率。
//
//输入格式
//输入共两行，第一行一个整数为确诊数 a，第二行一个整数为死亡数 b。
//
//输出格式
//输出仅一行，甲流死亡率，以百分数形式输出，精确到小数点后 3 位。
//
//输入输出样例
//输入 #1复制
//
//10433
//60
//输出 #1复制
//
//0.575%
//说明/提示
//数据规模与约定
//对于全部的测试点，保证 1≤a,b≤10 
//4
// 。
//
//提示
//在 C 风格输入输出中，百分号 % 可以这样输出：printf("%%");。

#include <cstdio>
using namespace std;

int main()
{
	int a, b;
	scanf("%d %d", &a, &b);
	printf("%.3lf%", b * 100.000 / a);
 
	return 0;
} 


//正解：
#include <cstdio>
#include <iostream>
using namespace std;

int main()
{
	int a,b;
	cin>>a>>b;
	printf("%.3f%%\n",b * 100.0 / a);	//要打印%，需要写%% 

    return 0;
 } 
