//# B2004 对齐输出
//
//## 题目描述
//
//读入三个整数，按每个整数占 $8$ 个字符的宽度，右对齐输出它们，按照格式要求依次输出三个整数，之间以一个空格分开。
//
//## 输入格式
//
//只有一行，包含三个整数 $a,b,c$。整数之间以一个空格分开。
//
//## 输出格式
//
//只有一行，按照格式要求依次输出三个整数，之间以一个空格分开。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//123456789 0 -1
//```
//
//### 输出 #1
//
//```
//123456789        0       -1
//```
//
//## 说明/提示
//
//对于 $100 \%$ 的数据，$-2^{31} \le a, b, c < 2^{31}$。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a, b, c;
    scanf("%d %d %d", &a, &b, &c);
    printf("%8d %8d %8d", a, b, c);

    return 0;
}
