//# B2014 与圆相关的计算
//
//## 题目描述
//
//给出圆的半径，求圆的直径、周长和面积。输入圆的半径实数 $r$，输出圆的直径、周长、面积，每个数保留小数点后 $4$ 位。圆周率取值为 $3.14159$。
//
//## 输入格式
//
//输入包含一个实数 $r$（$0<r \le 10000$），表示圆的半径。
//
//## 输出格式
//
//输出一行，包含三个数，分别表示圆的直径、周长、面积，数与数之间以一个空格分开，每个数保留小数点后 $4$ 位。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//3.0
//```
//
//### 输出 #1
//
//```
//6.0000 18.8495 28.2743
//```

#include <iostream>
#include <cstdio>
using namespace std;


int main()
{
    double r, d, c, s;
    double pi = 3.14159;
    cin >> r;
    d = 2 * r;
    c = 2 * pi * r;
    s = pi * (r*r);
    printf("%.4lf %.4lf %.4lf",d, c, s);

    return 0;
}
