//# B2013 温度表达转化
//
//## 题目描述
//
//利用公式 $C=5 \times (F - 32)/9$ ( 其中 $C$ 表示摄氏温度，$F$ 表示华氏温度）进行计算转化，输入华氏温度 $F$，输出摄氏温度 $C$，要求精确到小数点后 $5$ 位。
//
//## 输入格式
//
//输入一行，包含一个实数 $F$，表示华氏温度。（$F \ge - 459.67$）
//
//## 输出格式
//
//输出一行，包含一个实数，表示对应的摄氏温度，要求精确到小数点后 $5$ 位。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//41
//```
//
//### 输出 #1
//
//```
//5.00000
//```
//
//## 说明/提示
//
//（$F \ge - 459.67$）

#include <cstdio>
#include <iostream>

using namespace std;

int main()
{
	double F;
	double C;
	cin >> F;
	C = 5 * (F - 32) / 9;
	printf("%.5lf", C);
	
	return 0;
} 
