//# B2078 含 k 个 3 的数
//
//## 题目描述
//
//输入两个正整数 $m$ 和 $k$，其中 $1 \lt m \leq 10^{15}$，$1 \lt k \leq 15$ ，判断 $m$ 是否恰好含有 $k$ 个 $3$，如果满足条件，则输出 `YES`，否则，输出 `NO`。
//
//## 输入格式
//
//输入一行，为两个整数 $m,k$，中间用单个空格间隔。
//
//## 输出格式
//
//满足条件，则输出 `YES`，否则，输出 `NO`。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//43833 3
//```
//
//### 输出 #1
//
//```
//YES
//```


#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    long long k, m;
    int c = 0;
    cin >> m >> k;
    while(m)
    {
        if(m % 10 == 3)
            c++;
        m /= 10;
    }
    if(c == k)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
    
    return 0;
}
