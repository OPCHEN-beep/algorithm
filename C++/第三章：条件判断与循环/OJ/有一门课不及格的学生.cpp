//# B2044 有一门课不及格的学生
//
//## 题目描述
//
//给出一名学生的语文、数学、英语成绩，判断他是否恰好有一门课不及格（成绩小于 $60$ 分）。若该学生恰好有一门成绩不及格输出 $1$，否则输出 $0$。
//
//## 输入格式
//
//一行包含三个 $0 \sim 100$ 之间的整数，分别表示该生的语文、数学、英语成绩。
//
//## 输出格式
//
//该学生恰好有一门成绩不及格输出 $1$，否则输出 $0$。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//59 60 61
//```
//
//### 输出 #1
//
//```
//1
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//59 50 59
//```
//
//### 输出 #2
//
//```
//0
//```
//
//## 输入输出样例 #3
//
//### 输入 #3
//
//```
//100 100 90
//```
//
//### 输出 #3
//
//```
//0
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int c, m, e;
    cin >> c >> m >> e;
    if((c < 60 && m >= 60 && e >= 60) || (m < 60 && c >= 60 && e >= 60) || (e < 60 && m >= 60 && c >= 60))
        printf("1\n");
    else
        printf("0\n");
    
    return 0;
}
