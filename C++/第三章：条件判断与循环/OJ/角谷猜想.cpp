//# B2077 角谷猜想
//
//## 题目描述
//
//所谓角谷猜想，是指对于任意一个正整数，如果是奇数，则乘 $3$ 加 $1$，如果是偶数，则除以 $2$，得到的结果再按照上述规则重复处理，最终总能够得到 $1$。如，假定初始整数为 $5$，计算过程分别为 $16$ 、 $8$ 、 $4$ 、 $2$ 、 $1$。
//
//程序要求输入一个整数，将经过处理得到 $1$ 的过程输出来。
//
//## 输入格式
//
//一个正整数 $N(N \le 2,000,000)$。
//
//## 输出格式
//
//从输入整数到 $1$ 的步骤，每一步为一行，每一部中描述计算过程。最后一行输出 `End`。如果输入为 $1$，直接输出 `End`。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//5
//```
//
//### 输出 #1
//
//```
//5*3+1=16
//16/2=8
//8/2=4
//4/2=2
//2/2=1
//End
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    long long N;
    cin >> N;
    while(N != 1)
    {
        if(N % 2 == 0)
        {
            printf("%lld/2=%lld\n", N, N/2);
            N /= 2;
        }

        else
        {
            printf("%lld*3+1=%lld\n", N, N*3+1);
            N = N * 3 + 1;
        }
    } 
    printf("End\n");

    return 0;
}
