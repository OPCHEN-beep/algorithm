//# B2084 质因数分解
//
//## 题目描述
//
//已知正整数 $n$ 是两个不同的质数的乘积，试求出较大的那个质数。
//
//## 输入格式
//
//输入只有一行，包含一个正整数 $n$（$6<n<10^9$）。
//
//## 输出格式
//
//输出只有一行，包含一个正整数 $p$，即较大的那个质数。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//21
//```
//
//### 输出 #1
//
//```
//7
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int n = 0;
    cin >> n;
    int i = 0;
    for(i = 2; i < n; i++)
    {
        if(n % i == 0)
        {
            cout << n / i << endl;
            break;
        }
    }

    return 0;
}
