//# B2080 计算多项式的值
//
//## 题目描述
//
//假定多项式的形式为 $x^n+x^{(n-1)}+$ … $+x^2+x+1$，请计算给定单精度浮点数 $x$ 和正整数 $n$ 值的情况下这个多项式的值。多项式的值精确到小数点后两位，保证最终结果在 double 范围内。
//
//## 输入格式
//
//输入仅一行，包括 $x$ 和 $n$，用单个空格隔开。
//
//## 输出格式
//
//输出一个实数，即多项式的值，精确到小数点后两位。保证最终结果在 double 范围内。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//2.0 4
//```
//
//### 输出 #1
//
//```
//31.00
//```
//
//## 说明/提示
//
//$x$ 在 double 范围内且不为 $1$，$n \le 1000000$。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    double x;
    int n;
    double tmp = 1;
    double cnt = 1;
    cin >> x >> n;
    while(n--)
    {
        tmp *= x;
        cnt += tmp;
    }
    printf("%.2lf\n", cnt);

    return 0;
}
