//# B2036 输出绝对值
//
//## 题目描述
//
//输入一个浮点数 $n$，输出这个浮点数的绝对值。
//
//## 输入格式
//
//输入一个浮点数 $n$，其绝对值不超过 $10000$。
//
//## 输出格式
//
//输出 $n$ 的绝对值，保留到小数点后两位。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//-3.14
//```
//
//### 输出 #1
//
//```
//3.14
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    float n;
    cin >> n;
    if(n <= 0)
        n *= -1;
    else
        n *= 1;
    printf("%.2f", n);

    return 0;
}
