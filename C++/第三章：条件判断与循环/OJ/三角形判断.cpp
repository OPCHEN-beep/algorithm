//# B2050 三角形判断
//
//## 题目描述
//
//给定三个正整数，分别表示三条线段的长度，判断这三条线段能否构成一个三角形。
//
//## 输入格式
//
//输入共一行，包含三个正整数，分别表示三条线段的长度，数与数之间以一个空格分开。（三条边的长度均不超过 $10000$）
//
//## 输出格式
//
//如果能构成三角形，则输出 `1` ，否则输出 `0`。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//1 1 1
//```
//
//### 输出 #1
//
//```
//1
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//1 1 3
//```
//
//### 输出 #2
//
//```
//0
//```
//
//## 说明/提示
//
//构成三角形的条件：
//
//任意两边长度之和大于第三条边的长度。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a, b, c;
    cin >> a >> b >> c;
    if(a + b > c && a + c > b && b + c > b)
        printf("1\n");
    else
        printf("0\n");

    return 0;
}
