//# B2054 求平均年龄
//
//## 题目描述
//
//班上有学生若干名，给出每名学生的年龄（整数），求班上所有学生的平均年龄，保留到小数点后两位。
//
//## 输入格式
//
//第一行有一个整数 $n$（$1 \le n \le 100$），表示学生的人数。其后 $n$ 行每行有 $1$ 个整数，表示每个学生的年龄，取值为 $15$ 到 $25$。
//
//## 输出格式
//
//输出一行，该行包含一个浮点数，为要求的平均年龄，保留到小数点后两位。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//2
//18
//17
//```
//
//### 输出 #1
//
//```
//17.50
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    long long n;
    int age = 0;
    int cnt = 0;
    scanf("%lld\n", &n);
    for(long long i = n; i > 0; i--)
    {
        scanf("%d", &age);
        cnt += age;
    }
    double avg = cnt * 1.0 / n;
    printf("%.2lf\n", avg);

    return 0;
}
