#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int n, count = 0;
    cin >> n;
    
    if(n < 0)
    {
        n *= -1;
    }
    while(n)
    {
        int m = n % 10;
        count += m;
        n = n / 10;
    }
    printf("%d\n", count);
    
    return 0;
}
