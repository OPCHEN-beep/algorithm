//# B2032 等差数列末项计算
//
//## 题目描述
//
//等差数列是一个很有趣的数列，它的任何相邻两项的差相等。
//
//现在给出一个等差数列的前两项 $a_1,a_2$ 的值，求第 $n$ 项是多少。
//
//## 输入格式
//
//一行，包含三个整数 $a_1,a_2,n$（$-100 \le a_1,a_2 \le 100$，$0<n \le 1000$。）
//
//## 输出格式
//
//一个整数，即第 $n$ 项的值。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//1 4 100
//```
//
//### 输出 #1
//
//```
//298
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a1, a2, n;
    cin >> a1 >> a2 >> n;
    int d = a2 - a1;
    int an = a1 + (n - 1)*d;
    cout << an << endl;
    
    return 0;
}
