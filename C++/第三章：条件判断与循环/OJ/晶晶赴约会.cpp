//# B2045 晶晶赴约会
//
//## 题目描述
//
//晶晶的朋友贝贝约晶晶下周一起去看展览，但晶晶每周的 $1,3,5$ 有课必须上课，请帮晶晶判断她能否接受贝贝的邀请，如果能输出 `YES`；如果不能则输出 `NO`。
//
//## 输入格式
//
//输入有一行，贝贝邀请晶晶去看展览的日期，用数字 $1$ 到 $7$ 表示从星期一到星期日。
//
//## 输出格式
//
//输出有一行，如果晶晶可以接受贝贝的邀请，输出 `YES`，否则，输出 `NO`。注意 `YES` 和 `NO` 都是大写字母！
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//2
//```
//
//### 输出 #1
//
//```
//YES
//```

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int day;
    cin >> day;
    if(day == 1 || day == 3 || day == 5)
        printf("NO\n");
    else
        printf("YES\n");

    return 0;
}
