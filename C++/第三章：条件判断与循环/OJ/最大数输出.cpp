//# B2049 最大数输出
//
//## 题目描述
//
//输入三个整数，输出最大的数。
//
//## 输入格式
//
//输入为一行，包含三个整数，数与数之间以一个空格分开。
//
//## 输出格式
//
//输出一行，包含一个整数，即最大的整数。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//10 20 56
//```
//
//### 输出 #1
//
//```
//56
//```
//
//## 说明/提示
//
//### 数据规模与约定
//
//对于全部的测试点，保证输入的整数均在 32 位有符号整型 (`int`/`long int`) 范围内。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a, b, c, max;
    cin >> a >> b >> c;
    if(a >= b && a >= c)
        max = a;
    else if(b >= a && b >= c)
        max = b;
    else if(c >= a && c >= b)
        max = c;
    printf("%d\n", max);
    
    return 0;
}
