//# B2083 画矩形
//
//## 题目描述
//
//根据输入的四个参数：$a,b,c,f$ 参数，画出对应的矩形。
//
//前两个参数 $a,b$ 为整数，依次代表矩形的高和宽；
//
//第三个参数 $c$ 是一个字符，表示用来填充的矩形符号；
//
//第四个参数 $f$ 为整数，$0$ 代表空心，否则代表实心。
//
//具体例子请见样例。
//
//## 输入格式
//
//第一行：a,b,c,f。
//
//## 输出格式
//
//一个矩形。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//7 7 @ 0
//```
//
//### 输出 #1
//
//```
//@@@@@@@
//@     @
//@     @
//@     @
//@     @
//@     @
//@@@@@@@
//```
//
//## 说明/提示
//
//数据范围：
//
//$3 \le a \le 100$。
//
//$5 \le b \le 100$。
//
//保证 $c$ 是一个可见的字符（ASCII 码在 $[33,126]$ 内）。
//
//$0 \le f \le 1$。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a, b;
    char c;
    int f;
    cin >> a >> b >> c >> f;
    if(f)
    {
        for(int i = 1; i <= a; i++)
        {
            for(int j = 1; j <= b; j++)
            {
                cout << c;
            }
            cout << endl;
        }
    }
    else
    {
        for(int i = 1; i<= a; i++)
        {
            for(int j = 1; j <= b; j++)
            {
                if(i != 1 && i != a && j != 1 && j != b)
                {
                    cout << " ";
                }
                else
                {
                    cout << c;
                }
            }
            cout << endl;
        }
    }

    return 0;
}
