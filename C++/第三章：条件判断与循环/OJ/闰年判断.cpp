//# P5711 【深基3.例3】闰年判断
//
//## 题目描述
//
//输入一个年份，判断这一年是否是闰年，如果是输出 $1$，否则输出 $0$。
//
//$1582$ 年以来，闰年的定义：
//
//普通闰年：公历年份是 $4$ 的倍数，且不是 $100$ 的倍数的，为闰年（如 $2004$ 年、$2020$ 年等就是闰年）。
//
//世纪闰年：公历年份是整百数的，必须是 $400$ 的倍数才是闰年（如 $1900$ 年不是闰年，$2000$ 年是闰年）。
//
//## 输入格式
//
//输入一个正整数 $n$，表示年份。
//
//## 输出格式
//
//输出一行。如果输入的年份是闰年则输出 $1$，否则输出 $0$。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//1926
//```
//
//### 输出 #1
//
//```
//0
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//1900
//```
//
//### 输出 #2
//
//```
//0
//```
//
//## 输入输出样例 #3
//
//### 输入 #3
//
//```
//2000
//```
//
//### 输出 #3
//
//```
//1
//```
//
//## 输入输出样例 #4
//
//### 输入 #4
//
//```
//1996
//```
//
//### 输出 #4
//
//```
//1
//```
//
//## 说明/提示
//
//数据保证，$1582 \leq n \leq 2020$ 且年份为自然数。

#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int n;
    cin >> n;
    int m = (n % 4 == 0 && n % 100 != 0) || (n % 400 == 0) ? 1 : 0;
    cout <<m << endl;

    return 0;
}
