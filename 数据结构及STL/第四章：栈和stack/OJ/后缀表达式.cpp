//# P1449 后缀表达式
//
//## 题目描述
//
//所谓后缀表达式是指这样的一个表达式：式中不再引用括号，运算符号放在两个运算对象之后，所有计算按运算符号出现的顺序，严格地由左而右新进行（不用考虑运算符的优先级）。
//
//本题中运算符仅包含 $\texttt{+-*/}$。保证对于 $\texttt{/}$ 运算除数不为 0。特别地，其中 $\texttt{/}$ 运算的结果需要**向 0 取整**（即与 C++ `/` 运算的规则一致）。
//
//如：$\texttt{3*(5-2)+7}$ 对应的后缀表达式为：$\texttt{3.5.2.-*7.+@}$。在该式中，`@` 为表达式的结束符号。`.` 为操作数的结束符号。
//
//## 输入格式
//
//输入一行一个字符串 $s$，表示后缀表达式。
//
//## 输出格式
//
//输出一个整数，表示表达式的值。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//3.5.2.-*7.+@
//```
//
//### 输出 #1
//
//```
//16
//```
//
//## 输入输出样例 #2
//
//### 输入 #2
//
//```
//10.28.30./*7.-@
//```
//
//### 输出 #2
//
//```
//-7
//```
//
//## 说明/提示
//
//数据保证，$1 \leq |s| \leq 50$，答案和计算过程中的每一个值的绝对值不超过 $10^9$。

#include <iostream>
#include <stack>

using namespace std;

int main()
{
    stack<int> st;
    char ch;
    int num = 0;

    while(cin >> ch)
    {
        if(ch == '@')
            break;
        else if(ch >= '0' && ch <= '9')
        {
            num = num * 10 + ch - '0';
        }
        else if(ch == '.')
        {
            st.push(num);
            num = 0;
        }
        else
        {
            int b = st.top();
            st.pop();
            int a = st.top();
            st.pop();

            if(ch == '+')
                st.push(a + b);
            else if(ch == '-')
                st.push(a - b);
            else if(ch == '*')
                st.push(a * b);
            else
                st.push(a / b);
        }
    }
    cout << st.top() << endl;
    
    return 0;
}
