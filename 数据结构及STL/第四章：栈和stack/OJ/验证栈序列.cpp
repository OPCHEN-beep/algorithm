//# P4387 【深基15.习9】验证栈序列
//
//## 题目描述
//
//给出两个序列 pushed 和 poped 两个序列，其取值从 1 到 $n(n\le100000)$。已知入栈序列是 pushed，如果出栈序列有可能是 poped，则输出 `Yes`，否则输出 `No`。请注意，给定的序列一定是一个**排列**（即没有重复数字）。
//
//为了防止骗分，每个测试点有多组数据，不超过 $5$ 组。
//
//## 输入格式
//
//第一行一个整数 $q$，询问次数。
//
//接下来 $q$ 个询问，对于每个询问：
//
//第一行一个整数 $n$ 表示序列长度；
//
//第二行 $n$ 个整数表示入栈序列；
//
//第三行 $n$ 个整数表示出栈序列；
//
//## 输出格式
//
//对于每个询问输出答案。
//
//## 输入输出样例 #1
//
//### 输入 #1
//
//```
//2
//5
//1 2 3 4 5
//5 4 3 2 1
//4
//1 2 3 4
//2 4 1 3
//```
//
//### 输出 #1
//
//```
//Yes
//No
//```

#include <iostream>
#include <stack>
using namespace std;

const int N = 1e5 + 10;

int n;
int a[N], b[N];

int main()
{
    int Q;
    cin >> Q;
    while(Q--)
    {
        cin >> n;
        for(int i = 1; i <= n; i++)
            cin >> a[i];
        for(int i = 1; i <= n; i++)
            cin >> b[i];

        stack<int> st;

        int j = 1;
        for(int i = 1; i<= n; i++)
        {
            st.push(a[i]);

            while(j <= n && st.size() && st.top() == b[j])
            {
                //出栈
                st.pop();
                j++;
            }
        }
        if(st.size())
            cout << "No" << endl;
        else 
            cout << "Yes" << endl; 
    }

    return 0;
}
